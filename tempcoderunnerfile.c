//prgrm to check a charecter entered is a letter or not.if yes then do the uppercase nd lowercase conversion
#include<stdio.h>
int main(){
    char ch;
    printf("enter a charecter::");
    scanf("%c",&ch);
    if(ch>64&&ch<91){
        printf("\n%c is an uppercase charecter",ch);
        printf("\nThe lowercase of %c is %c",ch,ch+32);
    }
    else if(ch>96&&ch<123){
        printf("\n%c is a lowercase charecter",ch);
        printf("\nThe uppercase of %c is %c",ch,ch-32);
    }
    else{
        printf("\n%c is not an alphabet",ch);
    }
}